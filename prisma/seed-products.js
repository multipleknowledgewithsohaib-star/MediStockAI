async function main() {
  console.log('Clearing existing products...');
  // Clear dependent tables first
  await prisma.expectedDiscount.deleteMany();
  await prisma.productAlias.deleteMany();
  await prisma.purchaseItem.deleteMany();
  await prisma.saleItem.deleteMany();
  await prisma.batch.deleteMany();
  await prisma.product.deleteMany();
  
  let counter = 1;
  let totalProducts = 0;
  
  for (const [category, products] of Object.entries(medicines)) {
    const data = products.map(([name, brand, purchasePrice, salePrice, unitsPerPack]) => ({
      item_code: `ITEM${String(counter++).padStart(4, '0')}`,
      name,
      brand,
      category,
      purchasePrice,
      salePrice,
      stock: 0,
      unitsPerPack,
    }));
    
    await prisma.product.createMany({ data });
    totalProducts += products.length;
    console.log(`  ✅ ${category}: ${products.length} products seeded`);
  }
  
  console.log(`\n🎉 Total products seeded: ${totalProducts}`);
  console.log(`📋 Item codes: ITEM0001 to ITEM${String(counter - 1).padStart(4, '0')}`);
}

main()
  .catch((e) => { console.error('Error:', e); process.exit(1); })
  .finally(async () => { await prisma.$disconnect(); });
